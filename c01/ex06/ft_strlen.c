#include <stdio.h>

int	ft_strlen(char *str)
{
	int	l;

	l = 0;
	while (*(str++) != '\0')
	{
		l++;
	}
	return (l);
}

int	main()
{
	printf("%d", ft_strlen("rfuentes"));
}

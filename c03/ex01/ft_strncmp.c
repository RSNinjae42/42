int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	x;

	x = 0;
	while (x <= n && (s1[x] && s2[x]) && s1[x] == s2[x])
	{
		x++;
	}
	if (s1[x] == s2[x])
		return (0);
	else
		return (s1[x] - s2[x]);
}

#include <unistd.h>

int	main(int x, char **y)
{
	int	a;

	a = 0;
	if (y > 0)
	{
		while(y[0][a] != '\0')
		{
			write(1, &y[0][a], 1);
			a++;
		}
		write(1, "\n", 1);
	}
	return (0);
}